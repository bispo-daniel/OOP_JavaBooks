public class Author {
    private int authorId;
    private String authorName;

    public int getAuthorId(){
        return authorId;
    }
    public void setAuthorId(int idIn){
        authorId = idIn;
    }

    public String getAuthorName(){
        return authorName;
    }
    public void setAuthorName(String nameIn){
        authorName = nameIn;
    }
}
